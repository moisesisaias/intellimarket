function drawLine(i, pH, pW){
	var cv = document.getElementById("supermarket");
	var cvx = cv.getContext("2d");

	cvx.lineWidth = 6 * ((pH+pW)/2);

	var line = lines[i];

	cvx.beginPath();
	cvx.moveTo(line.x1 * pW, line.y1 * pH);
	cvx.lineTo(line.x2 * pW, line.y2 * pH);

	cvx.strokeStyle = '#ff0000';
	cvx.stroke();
	cvx.lineWidth = 1;
};